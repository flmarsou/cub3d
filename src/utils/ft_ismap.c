#include "cub3d.h"

bool	ft_ismap(char c)
{
	if (c == '1' || c == '0'
		|| c == ' ' || c == 'N'
		|| c == 'S' || c == 'W'
		|| c == 'E' || c == 'D')
		return (true);
	return (false);
}
